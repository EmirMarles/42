int	ft_isalpha(char *a)
{
	int	i;
	i = 0;
	while (a[i] != '\0')
	{
		if((a[i] >= 65 && a[i] <= 90) || (a[i] >= 97 && a[i] <= 122))
		{
			return (1);
		}
		i++;
	}
	return 0;
}

